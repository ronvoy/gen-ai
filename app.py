SYSTEM_PROMPT = (
    "You are a language model being evaluated on next-word prediction. "
    "Given a passage with its last word removed, predict the single missing word. "
    "Reply with ONLY that one word — no punctuation, no explanation."
)


def load_passages(file_path, sample_size=50, seed=42):
    with open(file_path, "r", encoding="utf-8") as f:
        passages = [line.strip() for line in f if line.strip()]
    random.seed(seed)
    if len(passages) > sample_size:
        passages = random.sample(passages, sample_size)
    return passages


def extract_target(passage):
    words = passage.split()
    target = words[-1].strip(".,!?;:'\"()[]{}").lower()
    context = " ".join(words[:-1])
    return context, target


def query_openrouter(api_key, model_id, context):
    start = time.time()
    try:
        resp = requests.post(
            "https://openrouter.ai/api/v1/chat/completions",
            headers={
                "Authorization": f"Bearer {api_key}",
                "Content-Type": "application/json",
            },
            json={
                "model": model_id,
                "messages": [
                    {"role": "system", "content": SYSTEM_PROMPT},
                    {
                        "role": "user",
                        "content": (
                            "Complete this passage with exactly one word. "
                            "Output ONLY the word:\n\n" + context
                        ),
                    },
                ],
                "max_tokens": 10,
                "temperature": 0,
            },
            timeout=60,
        )
        latency = time.time() - start
        if resp.status_code == 200:
            data = resp.json()
            raw = data["choices"][0]["message"]["content"].strip()
            prediction = raw.split()[0] if raw else ""
            prediction = prediction.strip(".,!?;:'\"()[]{}").lower()
            return prediction, latency, None
        return "", latency, f"HTTP {resp.status_code}: {resp.text[:200]}"
    except Exception as exc:
        return "", time.time() - start, str(exc)


def load_saved_results(model_key, benchmark_key):
    path = Path("results") / f"{model_key}_{benchmark_key}.json"
    if path.exists():
        with open(path, "r", encoding="utf-8") as f:
            return json.load(f)
    return None


def get_results(model_key, benchmark_key):
    saved = load_saved_results(model_key, benchmark_key)
    if saved:
        return saved
    if model_key in DEMO_RESULTS and benchmark_key in DEMO_RESULTS[model_key]:
        return DEMO_RESULTS[model_key][benchmark_key]
    return None


