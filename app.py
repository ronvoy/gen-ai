with tab_eval:
    st.header("Run Evaluation")

    bench_key = BENCHMARK_KEY_MAP[selected_benchmark]

    existing = get_results(selected_model_key, bench_key)
    is_demo = load_saved_results(selected_model_key, bench_key) is None

    st.info(
        f"**Model:** {selected_model_name}  |  "
        f"**Benchmark:** {selected_benchmark}  |  "
        f"**Sample Size:** {sample_size}"
    )

    if st.button("Run Live Evaluation", type="primary", disabled=not api_key):
        if not api_key:
            st.error("Please enter your OpenRouter API key in the sidebar.")
        else:
            bench_file = BENCHMARK_FILES[selected_benchmark]
            if not Path(bench_file).exists():
                st.error(f"Dataset file not found: {bench_file}")
            else:
                passages = load_passages(bench_file, sample_size)
                st.write(f"Loaded {len(passages)} passages.")

                progress_bar = st.progress(0.0)
                status_text = st.empty()
                results_container = st.empty()

                info = MODELS[selected_model_key]
                preds = []
                correct = 0
                errors_count = 0
                total_latency = 0.0

                for i, passage in enumerate(passages):
                    ctx, tgt = extract_target(passage)
                    pred, lat, err = query_openrouter(api_key, info["id"], ctx)

                    is_correct = pred == tgt
                    if is_correct:
                        correct += 1
                    if err:
                        errors_count += 1
                    total_latency += lat

                    preds.append({
                        "idx": i, "context_snippet": ctx[:300],
                        "target": tgt, "prediction": pred,
                        "correct": is_correct, "latency_s": round(lat, 3),
                        "error": err,
                    })

                    frac = (i + 1) / len(passages)
                    progress_bar.progress(frac)
                    status_text.text(
                        f"Passage {i+1}/{len(passages)} — "
                        f"Acc: {correct/(i+1):.1%} — "
                        f"Pred: '{pred}' | Target: '{tgt}'"
                    )
                    time.sleep(0.3)

                n = len(passages)
                metrics = {
                    "model_key": selected_model_key,
                    "model_name": info["name"],
                    "model_id": info["id"],
                    "provider": info["provider"],
                    "params": info["params"],
                    "benchmark": bench_key,
                    "total_passages": n,
                    "correct": correct,
                    "errors": errors_count,
                    "accuracy": round(correct / n, 4) if n else 0,
                    "error_rate": round(errors_count / n, 4) if n else 0,
                    "avg_latency_s": round(total_latency / n, 3) if n else 0,
                    "total_time_s": round(total_latency, 2),
                    "timestamp": datetime.now().isoformat(),
                }

                Path("results").mkdir(exist_ok=True)
                fpath = Path("results") / f"{selected_model_key}_{bench_key}.json"
                with open(fpath, "w", encoding="utf-8") as f:
                    json.dump({"metrics": metrics, "predictions": preds}, f, indent=2)

                st.success(f"Evaluation complete! Accuracy: {metrics['accuracy']:.1%}")
                existing = {"metrics": metrics, "predictions": preds}
                is_demo = False

    st.markdown("---")

    if existing:
        m = existing["metrics"]
        label = " *(demo data — run evaluation for real results)*" if is_demo else ""
        st.subheader(f"Results: {m['model_name']} on {selected_benchmark}{label}")

        c1, c2, c3, c4 = st.columns(4)
        c1.metric("Accuracy", f"{m['accuracy']:.1%}")
        c2.metric("Correct / Total", f"{m['correct']} / {m['total_passages']}")
        c3.metric("Avg Latency", f"{m['avg_latency_s']:.2f}s")
        c4.metric("Error Rate", f"{m['error_rate']:.1%}")

        if existing.get("predictions"):
            st.markdown("#### Per-Passage Predictions")
            pred_df = pd.DataFrame(existing["predictions"])
            display_cols = ["idx", "target", "prediction", "correct", "latency_s"]
            available_cols = [c for c in display_cols if c in pred_df.columns]
            st.dataframe(pred_df[available_cols], use_container_width=True, hide_index=True)
    else:
        st.warning("No results available. Run an evaluation or check that demo data is loaded.")

